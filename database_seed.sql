SET FOREIGN_KEY_CHECKS = 0;

-- Hapus semua tabel jika sudah ada (Reverse Dependency Order)
DROP TABLE IF EXISTS notifikasi;
DROP TABLE IF EXISTS applications;
DROP TABLE IF EXISTS position_skills;
DROP TABLE IF EXISTS positions;
DROP TABLE IF EXISTS recruitment_posts;
DROP TABLE IF EXISTS user_skills;
DROP TABLE IF EXISTS portofolios;
DROP TABLE IF EXISTS skills;
DROP TABLE IF EXISTS users;

-- Pembuatan Tabel (DDL)
CREATE TABLE users (
    id_user INT AUTO_INCREMENT PRIMARY KEY,
    email VARCHAR(255) NOT NULL UNIQUE,
    nama VARCHAR(255) NOT NULL,
    departemen VARCHAR(255),
    angkatan INT,
    bio TEXT
);

CREATE TABLE skills (
    id_skill INT AUTO_INCREMENT PRIMARY KEY,
    nama_skill VARCHAR(255) NOT NULL,
    kategori VARCHAR(100)
);

CREATE TABLE portofolios (
    id_port INT AUTO_INCREMENT PRIMARY KEY,
    id_user INT,
    judul VARCHAR(255) NOT NULL,
    link_asset VARCHAR(255),
    FOREIGN KEY (id_user) REFERENCES users(id_user) ON DELETE CASCADE
);

CREATE TABLE user_skills (
    id_user INT,
    id_skill INT,
    PRIMARY KEY (id_user, id_skill),
    FOREIGN KEY (id_user) REFERENCES users(id_user) ON DELETE CASCADE,
    FOREIGN KEY (id_skill) REFERENCES skills(id_skill) ON DELETE CASCADE
);

CREATE TABLE recruitment_posts (
    id_post INT AUTO_INCREMENT PRIMARY KEY,
    id_user INT,
    judul VARCHAR(255) NOT NULL,
    deskripsi TEXT,
    tgl_dibuat DATETIME DEFAULT CURRENT_TIMESTAMP,
    tgl_tutup DATETIME,
    FOREIGN KEY (id_user) REFERENCES users(id_user) ON DELETE CASCADE
);

CREATE TABLE positions (
    id_pos INT AUTO_INCREMENT PRIMARY KEY,
    id_post INT,
    nama_pos VARCHAR(255) NOT NULL,
    kuota INT DEFAULT 1,
    status VARCHAR(50) DEFAULT 'Buka',
    FOREIGN KEY (id_post) REFERENCES recruitment_posts(id_post) ON DELETE CASCADE
);

CREATE TABLE position_skills (
    id_pos INT,
    id_skill INT,
    PRIMARY KEY (id_pos, id_skill),
    FOREIGN KEY (id_pos) REFERENCES positions(id_pos) ON DELETE CASCADE,
    FOREIGN KEY (id_skill) REFERENCES skills(id_skill) ON DELETE CASCADE
);

CREATE TABLE applications (
    id_app INT AUTO_INCREMENT PRIMARY KEY,
    id_user INT,
    id_pos INT,
    tgl_daftar DATETIME DEFAULT CURRENT_TIMESTAMP,
    status VARCHAR(50) DEFAULT 'Pending',
    FOREIGN KEY (id_user) REFERENCES users(id_user) ON DELETE CASCADE,
    FOREIGN KEY (id_pos) REFERENCES positions(id_pos) ON DELETE CASCADE
);

CREATE TABLE notifikasi (
    id_notif INT AUTO_INCREMENT PRIMARY KEY,
    id_user INT,
    judul VARCHAR(255),
    pesan TEXT,
    kategori VARCHAR(100),
    id_terkait INT,
    status_baca BOOLEAN DEFAULT FALSE,
    tgl_dibuat DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (id_user) REFERENCES users(id_user) ON DELETE CASCADE
);

